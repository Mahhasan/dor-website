<p style="font-family:Century Gothic; font-size: 15px;">The Division of Research of Daffodil International University organized a workshop titled: “Formalizing Research Culture at DIU” on 18 September, 2019 at its premises. Professor Yousuf Mahbubul Islam, PhD, Vice Chancellor of the university facilitated the workshop as the key resource person. The Research Coordinator along with other faculty member(s) of each academic department participated at the workshop.</p>
<p style="font-family:Century Gothic; font-size: 15px;">At the outset, Professor Dr. Md. Kabirul Islam, Director, Division of Research, DIU made introductory deliberations where he clearly showed the different aspects and benefits of using the platform of Scopus for doing research in a befitting and easy manner and publish their papers in quality journals and increase international research collaborations. He also provided some directions and guidelines to the research coordinators for effective efforts and outcome-based research.</p>
<p style="font-family:Century Gothic; font-size: 15px;">Honorable Vice Chancellor of the university Professor Yousuf Mahbubul Islam, PhD, took the platform and conducted the entire workshop. He recognized the efforts of the research coordinators and faculty members of DIU which has increased the research activity manifold. The research culture that have been established, needs to make formal through documentation and publicity, he added. He then requested the participants to note down their ideas on how they would like to involve students in their research to build a research culture. After that their points were summarized and presented in three separate groups. Later on, they were requested to develop a flowchart as to how they will formalize the summarized research culture at their own department and each department developed the flowchart accordingly. The facilitator of the workshop observed the developed flowchart of each department. Finally he requested the participants of each department to consult with their Head and Dean of the faculty regarding approval of the developed flowchart of research culture and inform the office of the Vice Chancellor by each department within 20th October, 2019.</p>
