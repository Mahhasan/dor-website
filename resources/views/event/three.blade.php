                            <p style="font-family:Century Gothic; font-size: 15px; padding-top: 15px;" >Division of Research and Institutional Quality Assurance Cell of Daffodil International University jointly organized a workshop on “Building a Sustainable Research Culture at the Entity Level of Daffodil International University” on 14 September, 2017 at Banquet Hall, DIU. Professor Dr. Yousuf Mahbubul Islam, Vice Chancellor, Daffodil International University graced the program by being present as the Chief Guest while Professor Dr. S.M. Mahbub Ul Haque Majumder, Pro-Vice Chancellor of the university remained present as Special Guest. The session was chaired by Director IQAC, DIU Professor Dr. Md. Fokhray Hossain.</p>
                            <p style="font-family:Century Gothic; font-size: 15px;">Professor Dr. Md Kabirul Islam, Director, Division of Research of Daffodil International University managed the workshop and acted as key resource person. He discussed various aspects of research culture and strategies for implementation at the context of DIU. He also put emphasis on sharing faculty research interests amongst all the faculty members of a respective department in order to form different research groups and start collaborations amongst the groups. Priority needs to be given on publications in the Scopus Indexed Journals and Conferences as there are a lot of personal and professional benefits as well as improvement opportunity for ranking of the university. Students should be informed about the research colloquium and get involved to work with the teachers, he added.</p>
                            <p style="font-family:Century Gothic; font-size: 15px;"> The Chief Guest of the workshop defined the meaning of quality research. He opined that emphasis needs to be given on cross discipline and inter-department collaborations for conducting research and developing relationship amongst the faculty members. The Special Guest of the program underscored that DIU faculty members should be encouraged to apply for national and international research grants. The Chair of the session and Director, IQAC, DIU, Professor Dr. Md. Fokhray Hossain informed the participants that IQAC, DIU has been striving to ensure quality practice everywhere in the University and quality of research is one of the significant criteria of defining the quality and ranking of the University. </p>
                            <p style="font-family:Century Gothic; font-size: 15px;">Dr. Imran Mahmud, Associate Director, Division of Research of DIU also highlighted various aspects of writing articles and research papers in internationally recognized indexed journals, i.e. SCOPUS, ISI etc. He also showed the ranking of Private Universities of Bangladesh in terms of research publications by faculty members in SCOPUS indexed journals. About thirty young faculties of different departments took part in the workshop. At the end of the workshop, the participants received certificates from the Chief Guest and Vice Chancellor of the University, Professor Dr. Yousuf Mahbubul Islam.</p>

